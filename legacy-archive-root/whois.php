  async function doLookup(domain){
    const url = API_BASE + encodeURIComponent(domain);
    setLoading(true);
    
    try {
      const response = await fetch(url);
      const contentType = response.headers.get('content-type') || '';
      
      if (!response.ok) {
        throw new Error(`HTTP ${response.status}`);
      }

      let body;
      if (contentType.includes('application/json')) {
        body = await response.json();
      } else {
        body = await response.text();
      }

      setLoading(false);

      // Handle string response (raw WHOIS)
      if (typeof body === 'string') {
        box.innerHTML = '<p style="text-align:center;color:#64748b;">Displaying raw WHOIS data</p>';
        renderRaw(body, domain);
        return;
      }

      // Handle error response
      if (!body.ok || body.error) {
        showBuy(domain);
        return;
      }

      // Handle successful response
      if (body.data) {
        // Check if domain is available
        if (body.data.raw_text && /not found|no match|available/i.test(body.data.raw_text)) {
          showBuy(domain);
          return;
        }

        renderData(body.data);

        // Show raw WHOIS if available
        if (body.data.raw) {
          renderRaw(body.data.raw, domain);
        }
      } else {
        showBuy(domain);
      }

    } catch (error) {
      setLoading(false);
      box.innerHTML = `
        <div style="text-align:center;padding:20px;">
          <p style="color:#dc2626;font-size:16px;margin-bottom:10px;">
            ⚠️ Error fetching WHOIS data
          </p>
          <p style="color:#64748b;font-size:14px;">${error.message}</p>
          <button class="btn-lite" onclick="location.reload()" style="margin-top:16px;">
            Try Again
          </button>
        </div>`;
      rawWrap.style.display = 'none';
    }
  }

